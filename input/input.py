# Функция input() в Python используется для получения ввода от пользователя через стандартный поток ввода.

anything = input("Введите любое значние в командной строке и вы получите его ниже: ")
print(anything)
# => Значение, введенное пользователем в командной строке

name = input("Введите ваше имя: ")
print(f"Привет, {name}!")
# => Привет, {значение, введенное пользователем в командной строке}