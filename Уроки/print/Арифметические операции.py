print(4 + 1)
# => 5 // арифметическая операция: сложение

print(5 - 1)
# => 4 // арифметическая операция: вычитание

print(2 * 2)
# => 4 // арифметическая операция: умножение

print(2 ** 3)
# => 8 // арифметическая операция: возведение в степень

print(8 / 2)
# => 4 // арифметическая операция: деление // При делении двух чисел получается тип данных float

print(10 // 3)
# => 3 // арифметическая операция: целочисленное деление

print(10 % 3)
# => 1 // арифметическая операция: остаток от деления